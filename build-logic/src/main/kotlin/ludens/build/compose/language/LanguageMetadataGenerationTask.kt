package ludens.build.compose.language

import org.gradle.api.DefaultTask
import org.gradle.api.file.DirectoryProperty
import org.gradle.api.file.RegularFileProperty
import org.gradle.api.provider.Property
import org.gradle.api.tasks.Input
import org.gradle.api.tasks.InputDirectory
import org.gradle.api.tasks.OutputFile
import org.gradle.api.tasks.TaskAction

/**
 * Scans Compose resource value directories and generates
 * a Kotlin source file listing all available languages.
 *
 * The generated file provides:
 * - `availableLanguages`: ordered list of language tags
 * - `languageLabel(tag)`: composable function that resolves the display label
 *   for a given language tag using Compose string resources.
 */
abstract class LanguageMetadataGenerationTask : DefaultTask() {

    /** Kotlin package used by the generated source file. */
    @get:Input
    abstract val packageName: Property<String>

    /** Top-level object name written to the generated Kotlin source. */
    @get:Input
    abstract val objectName: Property<String>

    /** Directory containing Compose `values` resource directories. */
    @get:InputDirectory
    abstract val resourceDir: DirectoryProperty

    /** Output file that will contain the generated Kotlin source. */
    @get:OutputFile
    abstract val outputFile: RegularFileProperty

    @TaskAction
    fun generate() {
        val dir = resourceDir.get().asFile
        val languages = discoverLanguages(dir)

        val content = buildString {
            appendLine("package ${packageName.get()}")
            appendLine()
            appendLine("import androidx.compose.runtime.Composable")
            appendLine("import ludens.composeapp.generated.resources.Res")
            appendLine("import ludens.composeapp.generated.resources.unknown_language")
            languages.map { it.labelResourceName }.distinct().forEach { resourceName ->
                    appendLine("import ludens.composeapp.generated.resources.$resourceName")
                }
            appendLine("import org.jetbrains.compose.resources.StringResource")
            appendLine("import org.jetbrains.compose.resources.stringResource")
            appendLine()
            appendLine("// Autogenerated file. Do not edit manually.")
            appendLine(
                "// Languages discovered from composeResources directories: ${
                languages.joinToString(
                    ", "
                ) { it.tag }
            }")
            appendLine()
            appendLine("object ${objectName.get()} {")
            appendLine("    /** Ordered list of available language tags. */")
            appendLine("    val availableLanguages: List<String> = listOf(")
            languages.forEach { language ->
                appendLine("        \"${language.tag}\",")
            }
            appendLine("    )")
            appendLine()
            appendLine("    /** Resolves the [StringResource] for a language tag's display label. */")
            appendLine("    fun labelResource(tag: String): StringResource = when (tag) {")
            languages.forEach { language ->
                appendLine("        \"${language.tag}\" -> Res.string.${language.labelResourceName}")
            }
            appendLine("        else -> Res.string.unknown_language")
            appendLine("    }")
            appendLine()
            appendLine("    /**")
            appendLine("     * Returns the localized display label for the given language tag.")
            appendLine("     * Falls back to [stringResource] for unknown tags.")
            appendLine("     */")
            appendLine("    @Composable")
            appendLine("    fun languageLabel(tag: String): String {")
            appendLine("        return stringResource(labelResource(tag))")
            appendLine("    }")
            appendLine("}")
        }

        outputFile.get().asFile.apply {
            parentFile.mkdirs()
            writeText(content)
        }
    }

}
